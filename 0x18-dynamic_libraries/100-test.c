#include <stdio.h>
#include "main.h"
/**
 * add - multiply two variables
 * @a: variable 1
 * @b: variable 2
 * Return: addition of thwo variables
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtract two variables
 * @a: variable 1
 * @b: variable 2
 * Return: subtraction of two variables
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 *  mul - multiply two variables
 *  @a: var 1
 *  @b: var 2
 *  Return: mul of two var
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divides two numbers
 * @a: var 1
 * @b: var 2
 * Return: division of two var
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - find the modulus
 * @a: var 1
 * @b: var 2
 * Return: modulusof tow numbers
 */
int mod(int a, int b)
{
	return (a % b);
}
